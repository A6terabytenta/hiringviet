package vn.com.hiringviet.dto;

public class StatisticDTO {
	private int totalMember;
	private int totalCompany;
	private int totalConnect;
	private int totalPost;
	private int totalEmail;

	public int getTotalMember() {
		return totalMember;
	}

	public void setTotalMember(int totalMember) {
		this.totalMember = totalMember;
	}

	public int getTotalCompany() {
		return totalCompany;
	}

	public void setTotalCompany(int totalCompany) {
		this.totalCompany = totalCompany;
	}

	public int getTotalConnect() {
		return totalConnect;
	}

	public void setTotalConnect(int totalConnect) {
		this.totalConnect = totalConnect;
	}

	public int getTotalPost() {
		return totalPost;
	}

	public void setTotalPost(int totalPost) {
		this.totalPost = totalPost;
	}

	public int getTotalEmail() {
		return totalEmail;
	}

	public void setTotalEmail(int totalEmail) {
		this.totalEmail = totalEmail;
	}

}
